"""LLM proposal saver.

Saves LLM-generated suggestions as untrusted Markdown files for
human review.  All output is clearly marked as untrusted.

Usage:
    python -m scripts.propose --llm --save-dir output/proposals
"""

from __future__ import annotations

from datetime import datetime, timezone
from pathlib import Path

from app.evaluation.llm_adapter import LLMProposalResult
from app.evaluation.proposal import ProposalDocument

# ---------------------------------------------------------------------------
# Untrusted marker
# ---------------------------------------------------------------------------

UNTRUSTED_HEADER = """\
> **UNTRUSTED SUGGESTION** — This content was generated by an LLM
> and has NOT been reviewed or validated. Do NOT apply these changes
> without thorough human review. All suggestions may contain errors,
> hallucinations, or unsafe recommendations.

---

"""

UNTRUSTED_FOOTER = """\

---

> **UNTRUSTED SUGGESTION** — All content above was generated by an LLM.
> Review carefully before taking any action. Do not commit, deploy, or
> merge any suggested changes without human verification.
"""


# ---------------------------------------------------------------------------
# Rendering
# ---------------------------------------------------------------------------


def render_llm_proposal_markdown(
    doc: ProposalDocument,
    results: list[LLMProposalResult],
) -> str:
    """Render LLM proposal results as untrusted Markdown.

    Args:
        doc: The original proposal document with context.
        results: LLM suggestion results for each proposal.

    Returns:
        Markdown string with clear untrusted markers.
    """
    lines = [
        UNTRUSTED_HEADER,
        "# LLM Detector Improvement Suggestions",
        "",
        f"**Generated at:** {datetime.now(timezone.utc).isoformat()}",
        f"**Run ID:** `{doc.run_id}`",
        f"**Total suggestions:** {len(results)}",
        "",
        "## Overview",
        "",
        "This document contains AI-generated suggestions for improving "
        "detector rules based on evaluation warnings. All suggestions "
        "are **untrusted** and require human review before implementation.",
        "",
        "---",
        "",
    ]

    # Group results by detector
    by_detector: dict[str, list[LLMProposalResult]] = {}
    for result in results:
        key = result.detector_name
        if key not in by_detector:
            by_detector[key] = []
        by_detector[key].append(result)

    for detector_name, detector_results in sorted(by_detector.items()):
        lines.append(f"## {detector_name}")
        lines.append("")

        for i, result in enumerate(detector_results, 1):
            suggestion = result.suggestion
            lines.append(f"### Suggestion {i}: {result.site_name}")
            lines.append("")
            lines.append(f"- **Provider:** {result.provider}")
            lines.append(f"- **Model:** {result.model}")
            lines.append(f"- **Confidence:** {suggestion.confidence:.0%}")
            lines.append("")

            # Likely cause
            lines.append("#### Likely Cause")
            lines.append("")
            lines.append(suggestion.likely_cause)
            lines.append("")

            # Proposed rule adjustment
            lines.append("#### Proposed Rule Adjustment")
            lines.append("")
            lines.append(suggestion.proposed_rule_adjustment)
            lines.append("")

            # Expected risks
            if suggestion.expected_risks:
                lines.append("#### Expected Risks")
                lines.append("")
                for risk in suggestion.expected_risks:
                    lines.append(f"- {risk}")
                lines.append("")

            # Tests to add
            if suggestion.tests_to_add:
                lines.append("#### Tests to Add")
                lines.append("")
                for test in suggestion.tests_to_add:
                    lines.append(f"- {test}")
                lines.append("")

            # Uncertainty
            lines.append("#### Uncertainty")
            lines.append("")
            lines.append(suggestion.uncertainty)
            lines.append("")

            lines.append("---")
            lines.append("")

    # Footer
    lines.append(UNTRUSTED_FOOTER)

    return "\n".join(lines)


def render_llm_proposal_json(
    doc: ProposalDocument,
    results: list[LLMProposalResult],
) -> str:
    """Render LLM proposal results as JSON with untrusted marker.

    The JSON output includes an `untrusted` flag and the results
    are wrapped in a metadata object.
    """
    import json

    output = {
        "untrusted": True,
        "warning": (
            "This content was generated by an LLM and has NOT been "
            "reviewed or validated. Do NOT apply without human review."
        ),
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "run_id": doc.run_id,
        "total_suggestions": len(results),
        "results": [r.model_dump(mode="json") for r in results],
    }

    return json.dumps(output, indent=2, default=str)


# ---------------------------------------------------------------------------
# File saving
# ---------------------------------------------------------------------------


def save_proposals(
    doc: ProposalDocument,
    results: list[LLMProposalResult],
    output_dir: Path,
    format: str = "markdown",
) -> Path:
    """Save LLM proposal results to a file.

    Args:
        doc: The original proposal document.
        results: LLM suggestion results.
        output_dir: Directory to save the file in.
        format: Output format ('markdown' or 'json').

    Returns:
        Path to the saved file.
    """
    output_dir.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    run_id_short = str(doc.run_id)[:8]

    if format == "json":
        filename = f"proposals_{run_id_short}_{timestamp}.json"
        content = render_llm_proposal_json(doc, results)
    else:
        filename = f"proposals_{run_id_short}_{timestamp}.md"
        content = render_llm_proposal_markdown(doc, results)

    filepath = output_dir / filename
    filepath.write_text(content, encoding="utf-8")

    return filepath
